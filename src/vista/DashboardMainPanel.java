     public DashboardMainPanel(Connection conexion) {
        this.conexion = conexion;
        this.ventaDAO = new VentaDAO(conexion);
        initComponents();
        agregarGrafico();
    }
    private void agregarGrafico() {
        DefaultCategoryDataset dataset = crearDataset();
        JFreeChart chart = ChartFactory.createBarChart(
                "Productos MÃ¡s Vendidos",
                "Producto",
                "Cantidad Vendida",
                dataset,
                PlotOrientation.VERTICAL,
                true,
                true,
                false);

        ChartPanel chartPanel = new ChartPanel(chart);
        chartPanel.setPreferredSize(new Dimension(800, 600));

        setLayout(new BorderLayout());
        add(chartPanel, BorderLayout.CENTER);
    }

    private DefaultCategoryDataset crearDataset() {
        DefaultCategoryDataset dataset = new DefaultCategoryDataset();
        try {
            Map<String, Integer> productosMasVendidos = ventaDAO.obtenerProductosMasVendidos();
            for (Map.Entry<String, Integer> entry : productosMasVendidos.entrySet()) {
                dataset.addValue(entry.getValue(), "Productos", entry.getKey());
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
        return dataset;
    }

